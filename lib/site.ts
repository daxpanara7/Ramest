/**
 * Candidate images for the Organization entity, best first. Google picks what
 * it shows in AI Overviews and knowledge panels from these; a logo alone is a
 * weak signal because it is not a photograph of the company.
 */
function organizationImages() {
  const images = [
    // Real photo first — the only candidate Google treats as a company image.
    ...(SITE.teamPhoto ? [`${SITE.url}${SITE.teamPhoto}`] : []),
    `${SITE.url}${SITE.ogImage}`,
    `${SITE.url}${SITE.logo}`,
  ];
  return images;
}

