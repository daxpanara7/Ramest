type PageMetaInput = {
  title: string;
  description: string;
  path?: string;
  image?: string;
  /** When true, use title as-is (no template suffix). Default false. */
  absoluteTitle?: boolean;
  /** Keep the page out of the index (404s, utility routes). */
  noindex?: boolean;
};

/** Consistent Metadata for every page — titles, OG, Twitter, canonical. */
export function createPageMetadata({
  title,
  description,
  path = "/",
  image = SITE.ogImage,
  absoluteTitle = false,
  noindex = false,
}: PageMetaInput): Metadata {
  const url = path === "/" ? SITE.url : `${SITE.url}${path}`;
  const ogTitle = absoluteTitle ? title : `${title} | ${SITE.name}`;

  return {
    title: absoluteTitle ? { absolute: title } : title,
    description,
    alternates: { canonical: url },
    openGraph: {
      type: "website",
      locale: SITE.locale,
      url,
      siteName: SITE.name,
      title: ogTitle,
      description,
      images: [{ url: image, width: 1200, height: 630, alt: ogTitle }],
    },
    twitter: {
      card: "summary_large_image",
      title: ogTitle,
      description,
      images: [image],
    },
    robots: noindex
      ? { index: false, follow: false, googleBot: { index: false, follow: false } }
      : { index: true, follow: true, googleBot: { index: true, follow: true } },
  };
}

